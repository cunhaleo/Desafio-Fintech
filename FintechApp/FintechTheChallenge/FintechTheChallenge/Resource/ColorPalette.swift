import UIKit

extension UIColor {
    
    static func forestGreen() -> UIColor { UIColor(named: "forestGreen") ?? .white }
    
    static func primaryColor() -> UIColor { UIColor(named: "PrimaryColor") ?? .white }
    
    static func secondaryColor() -> UIColor { UIColor(named: "SecondaryColor") ?? .white }
}
